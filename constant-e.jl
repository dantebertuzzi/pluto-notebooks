    O número $e$ não foi inventado por uma única pessoa, mas surgiu ao longo da história da matemática.

    **Jacob Bernoulli (1683)** foi o primeiro a descobrir o valor de $e$ ao estudar juros compostos, analisando o limite:

    $$\left(1 + \frac{1}{n}\right)^n$$

    e percebendo que ele converge para aproximadamente:

    $$2.71828\ldots$$

    Posteriormente, **Leonhard Euler** formalizou o conceito, introduziu a notação $e$ e desenvolveu toda a teoria envolvendo funções exponenciais e logaritmos naturais.

    👉 Hoje, $e$ é conhecido como a **constante de Euler** e é fundamental em cálculo, probabilidade e modelagem matemática.
